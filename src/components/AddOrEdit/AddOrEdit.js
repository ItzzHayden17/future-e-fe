import React ,{useState} from 'react'
import './AddOrEdit.css'
import serverUrl from '../../serverUrl'
import axios from 'axios'

const AddOrEdit = (props) => {

 const [company,setCompany] = useState(props.company)

    function handleCloseModal(e){
        props.onClick(e)
    }

    function handleChange(e){
        const {name,value} = e.target
        setCompany((prev)=>({
            ...prev,
            [name]:value
        }))
    }


  return (
    <div className='AddOrEdit'>

        <div className='form'>
            <p onClick={handleCloseModal}>X</p>
            {props.mode === 'add' && <>
        <h1>Add a Company</h1>
        <form onSubmit={(e) => {
                e.preventDefault();

                const formData = new FormData(e.target);

                const data = {
                  companyName: formData.get('companyName'),
                  password: formData.get('password'),
                  towingNumber: formData.get('towingNumber'),
                  policyNumber : formData.get('policyNumber')
                }

                axios.post(`${serverUrl}/add-company`, data)
                  .then(res => window.location.reload())
                  .catch(err => console.error(err));
    }}>
            <label>Company name</label>
            <input placeholder='Company name' name='companyName'></input>

            <label>Password</label>
            <input placeholder='Password' name='password'></input>
            
            <label>Confirm Password</label>
            <input placeholder='Confirm Password'></input>

            <label>Towing service number</label>
            <input placeholder='Number' type='number' name='towingNumber'></input>
            
            <label>Policy number</label>
            <input placeholder='Policy number' type='text' name='policyNumber'></input>

            <button>Add</button>
        </form>
        </>}
        {props.mode === 'edit' && (
  <>
    <h1>Edit Company</h1>
    <form onSubmit={(e) => {
                e.preventDefault();
                const formData = new FormData(e.target);

                const data = {
                  id :formData.get('id'),
                  companyName: formData.get('companyName'),
                  password: formData.get('password'),
                  towingNumber: formData.get('towingServiceNumber'),
                  policyNumber: formData.get('policyNumber')
                }
                console.log(data);

                axios.post(`${serverUrl}/edit-company`, data)
                  .then(res => window.location.reload())
                  .catch(err => console.error(err));
    }}>
      <input type="hidden" name="id" value={company.id} />
      <label>Company name</label>
      <input 
        name="companyName"
        placeholder="Company name"
        value={company.companyName || ""}
        onChange={handleChange}
      />

      <label>Password</label>
      <input 
        type="password"
        name="password"
        placeholder="Password"
        value={company.password || ""}
        onChange={handleChange}
      />
      
      <label>Confirm Password</label>
      <input 
        type="password"
        name="confirmPassword"
        placeholder="Confirm Password"
        onChange={handleChange}
      />

      <label>Towing service number</label>
      <input 
        name="towingServiceNumber"
        placeholder="Towing service number"
        value={company.towingServiceNumber || ""}
        onChange={handleChange}
      />

      <label>Policy number</label>
      <input 
        name="policyNumber"
        placeholder="Policy number"
        value={company.policyNumber || ""}
        onChange={handleChange}
      />

      <button type="submit">Save Changes</button>
    </form>
  </>
)}

        </div>
    </div>
  )
}

export default AddOrEdit